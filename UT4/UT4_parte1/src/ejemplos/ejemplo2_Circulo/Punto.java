package ejemplos.ejemplo2_Circulo;

/**
 * Representa a un punto en el espacio bidimensional
 *
 * @author
 */
public class Punto {
    private int x;
    private int y;

    /**
     * Constructor de la clase Punto
     */
    public Punto(int x, int y) {
        this.x = x;
        this.y = y;
    }

}
