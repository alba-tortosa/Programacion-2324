/** Ejercicio 2.15
 * Evalúa las siguientes expresiones matemáticas:
 * 25 + 20 - 15
 * 20 * 10 + 15 * 10
 * 20 * 10 / 2 - 20 + 3 * 3
 * 15 / 10 * 2 + 3 / 4 * 8
 * 46 % 9 + 4 * 4 - 2
 * 45 + 43 % 5 * (23 + 3 % 2)
 * 1.5 * 3


 */


public class Ejercicio_2_15 {
    public static void main(String[] args) {

        System.out.println(25 + 20 - 15); //30
        System.out.println(20 * 10 + 15 * 10); //350
        System.out.println(20 * 10 / 2 - 20 + 3 * 3); //89
        System.out.println(15 / 10 * 2 + 3 / 4 * 8); //2
        System.out.println(46 % 9 + 4 * 4 - 2); //15
        System.out.println(45 + 43 % 5 * (23 + 3 % 2)); //117
        System.out.println(1.5 * 3); //4.5

    }

}